    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String Bname = jTextField1.getText();
        try
         {
             Connection con = ConnectionProvider.getCon();
             Statement st = con.createStatement();
            
             ResultSet findbook= st.executeQuery("select * from book where Book_Name = '"+Bname+"'");
             
             if(findbook.next())
             {
                 String book = findbook.getString(1);
                 String self = findbook.getString(6);
                 JOptionPane.showMessageDialog(null,"The book "+book+" is available in self no "+ self);
                 
                 
             }
            else
             {
               JOptionPane.showMessageDialog(null,"The book"+Bname+"is not available in the library");
             }
         }
        
        catch(Exception e)
        {
             
        }
        
        
             
    }//GEN-LAST:event_jButton8ActionPerformed

